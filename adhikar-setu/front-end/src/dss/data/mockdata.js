// Helper functions to get unique values for dropdowns
export const getUniqueVillages = () => {
  return [...new Set(mockBeneficiaries.map(b => b.village))].sort();
};

export const getUniqueDistricts = () => {
  return [...new Set(mockBeneficiaries.map(b => b.district))].sort();
};

export const getUniqueStates = () => {
  return [...new Set(mockBeneficiaries.map(b => b.state))].sort();
};

export const getDistrictsByVillage = (village) => {
  if (!village) return getUniqueDistricts();
  return [...new Set(mockBeneficiaries
    .filter(b => b.village === village)
    .map(b => b.district))].sort();
};

export const getVillagesByState = (state) => {
  if (!state) return getUniqueVillages();
  return [...new Set(mockBeneficiaries
    .filter(b => b.state === state)
    .map(b => b.village))].sort();
};
export const getBeneficiariesByFilters = (state = '', village = '', beneficiaryName = '') => {
  return mockBeneficiaries.filter(b => {
    if (state && b.state !== state) return false;
    if (village && b.village !== village) return false;
    if (beneficiaryName && b.name !== beneficiaryName) return false;
    return true;
  });
};
export const getBeneficiariesByVillage = (village) => {
  if (!village) return mockBeneficiaries;
  return mockBeneficiaries.filter(b => b.village === village);
};

